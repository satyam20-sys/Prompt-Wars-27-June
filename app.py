@app.route("/journal", methods=["POST"])
def save_journal():
    """Validates and saves a new journal entry without stress scores."""
    data = request.json or {}
    text = data.get("text", "")
    mood = data.get("mood", "")
    exam = data.get("exam", "")
    exam_date = data.get("exam_date", None)

    # Validation
    sanitized_text = sanitize_text(text)
    if not sanitized_text:
        return jsonify({"error": "Journal text cannot be empty."}), 400

    if not mood:
        return jsonify({"error": "Mood parameter is required."}), 400

    if not exam:
        return jsonify({"error": "Target exam is required."}), 400

    # Read existing database
    entries = read_journal_data()

    # Construct new entry
    new_entry = {
        "id": str(uuid.uuid4()),
        "timestamp": datetime.datetime.utcnow().isoformat() + "Z",
        "mood": mood,
        "exam": exam,
        "exam_date": exam_date if exam_date else None,
        "text": sanitized_text,
        "stress_level": None,
        "emotions": []
    }

    entries.append(new_entry)
    write_journal_data(entries)

    return jsonify(new_entry), 201


@app.route("/analyze", methods=["POST"])
def analyze_journal():
    """Calls Gemini to analyze the text of an entry, updating it in the datastore."""
    data = request.json or {}
    entry_id = data.get("id")

    if not entry_id:
        return jsonify({"error": "Entry ID is required."}), 400

    entries = read_journal_data()
    matching_entry = next((e for e in entries if e["id"] == entry_id), None)

    if not matching_entry:
        return jsonify({"error": f"Journal entry with ID {entry_id} not found."}), 404

    system_prompt = (
        "You are an empathetic mental wellness coach for Indian students under exam pressure. "
        "Analyze this ACTUAL journal entry and mood ONLY. Return valid JSON:\n"
        "{\n"
        "  \"stress_level\": 1-10 integer,\n"
        "  \"detected_emotions\": [exactly 3 emotion strings],\n"
        "  \"hidden_triggers\": [2-3 India-specific triggers from the text],\n"
        "  \"coping_strategy\": \"specific actionable tip (2-3 sentences)\",\n"
        "  \"mindfulness_exercise\": \"2-minute exercise for their stress\",\n"
        "  \"motivational_message\": \"personalized, mentions their exam\",\n"
        "  \"india_context\": \"culturally relevant insight from the entry\",\n"
        "  \"safety_flag\": true or false\n"
        "}\n"
        "Return only valid JSON. Do not invent or assume anything not present in the journal entry."
    )

    user_prompt = f"Journal Entry text: '{matching_entry['text']}'\nMood Logged: '{matching_entry['mood']}'"

    try:
        analysis = call_gemini_json(user_prompt, system_instruction=system_prompt)
        
        # Validate that the returned data matches the expected structure
        required_keys = [
            "stress_level", "detected_emotions", "hidden_triggers", 
            "coping_strategy", "mindfulness_exercise", "motivational_message", 
            "india_context", "safety_flag"
        ]
        for key in required_keys:
            if key not in analysis:
                raise ValueError(f"Missing expected key '{key}' in Gemini output.")

        # Update entry in data store
        matching_entry["stress_level"] = int(analysis["stress_level"])
        matching_entry["emotions"] = list(analysis["detected_emotions"])
        
        write_journal_data(entries)
        
        return jsonify(analysis), 200

    except Exception as e:
        return jsonify({"error": f"Failed to complete AI analysis: {str(e)}"}), 502


@app.route("/burnout-predict", methods=["POST"])
def predict_burnout():
    """Predicts burnout risk trends based on 3+ journal entries."""
    entries = read_journal_data()

    if len(entries) < MIN_BURNOUT_ENTRIES:
        return jsonify({"insufficient_data": True}), 200

    # Format the real entries for the prompt
    formatted_entries = []
    for idx, entry in enumerate(entries):
        formatted_entries.append(
            f"Entry {idx + 1} ({entry['timestamp']}): Mood={entry['mood']}, Stress={entry['stress_level']}, Text='{entry['text']}'"
        )
    entries_text = "\n".join(formatted_entries)

    system_prompt = (
        "Analyze ONLY these actual journal entries (do not invent any data). "
        "Based on the real stress pattern, return JSON:\n"
        "{\n"
        "  \"burnout_risk\": \"low|medium|high|critical\",\n"
        "  \"days_until_burnout\": integer or null,\n"
        "  \"warning_signs\": [3 signs actually found in entries],\n"
        "  \"intervention\": \"3-step plan based on real patterns\"\n"
        "}\n"
        "Return only valid JSON, no markdown, no invented data."
    )

    try:
        prediction = call_gemini_json(entries_text, system_instruction=system_prompt)
        
        # Verify required keys
        required_keys = ["burnout_risk", "days_until_burnout", "warning_signs", "intervention"]
        for key in required_keys:
            if key not in prediction:
                raise ValueError(f"Missing expected key '{key}' in Gemini output.")
                
        return jsonify(prediction), 200
    except Exception as e:
        return jsonify({"error": f"Failed to calculate burnout prediction: {str(e)}"}), 502


