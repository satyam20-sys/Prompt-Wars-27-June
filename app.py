@app.route("/dashboard", methods=["GET"])
def get_dashboard_data():
    """Calculates daily statistics, streaks, and top emotions from entries."""
    try:
        entries = read_journal_data()
    except (ValueError, IOError) as e:
        return jsonify({"error": str(e)}), 500

    if not entries:
        return jsonify({"entries": [], "empty": True}), 200

    # Calculate stress by date
    stress_by_date = {}
    # Count of stress levels per date to average later
    stress_counts = {}

    # Calculate top emotions
    top_emotions = {}

    # Unique dates set for streak calculation
    unique_dates = set()

    for entry in entries:
        # Extract YYYY-MM-DD from ISO format timestamp
        dt_str = entry["timestamp"].split("T")[0]
        unique_dates.add(dt_str)

        # Average stress levels
        stress = entry.get("stress_level")
        if stress is not None:
            stress_by_date[dt_str] = stress_by_date.get(dt_str, 0.0) + float(stress)
            stress_counts[dt_str] = stress_counts.get(dt_str, 0) + 1

        # Emotions count
        for emo in entry.get("emotions", []):
            if emo:
                top_emotions[emo] = top_emotions.get(emo, 0) + 1

    # Finish average stress calculation
    for dt in stress_by_date:
        if stress_counts[dt] > 0:
            stress_by_date[dt] = round(stress_by_date[dt] / stress_counts[dt], 1)

    # Streak Calculation
    sorted_dates = sorted([datetime.datetime.strptime(d, "%Y-%m-%d").date() for d in unique_dates], reverse=True)
    streak = 0
    if sorted_dates:
        today = datetime.datetime.utcnow().date()
        yesterday = today - datetime.timedelta(days=1)

        # Streak is active only if they journaled today or yesterday
        if sorted_dates[0] in (today, yesterday):
            streak = 1
            current_date = sorted_dates[0]
            for next_date in sorted_dates[1:]:
                if (current_date - next_date).days == 1:
                    streak += 1
                    current_date = next_date
                elif (current_date - next_date).days == 0:
                    continue  # Same day entry
                else:
                    break  # Gap in dates

    return jsonify({
        "entries_count": len(entries),
        "stress_by_date": stress_by_date,
        "top_emotions": top_emotions,
        "streak_days": streak
    }), 200


@app.route("/chat", methods=["POST"])
def chat():
    """Facilitates an empathetic chat response from Gemini."""
    data = request.json or {}
    messages = data.get("messages", [])
    exam = data.get("exam", "General")
    mood = data.get("mood", "Neutral")

    if not messages:
        return jsonify({"error": "No messages provided."}), 400

    system_instruction = (
        f"You are an empathetic, supportive mental wellness coach for Indian students preparing for competitive exams "
        f"like JEE, NEET, CUET, CAT, GATE, UPSC, or Board exams. "
        f"The student is currently preparing for {exam} and is feeling {mood}. "
        f"Keep your response concise, culturally relevant (referencing the intense pressure in India), supportive, "
        f"and limited to 2-3 sentences. Do not offer clinical advice, and act as a friendly guide. "
        f"Respond with empathy."
    )

    prompt_lines = []
    # Take at most the last 5 messages for context
    for msg in messages[-5:]:
        role = msg.get("role", "user")
        content = msg.get("content", "")
        speaker = "Student" if role == "user" else "Coach"
        prompt_lines.append(f"{speaker}: {content}")
    
    prompt_lines.append("Coach:")
    prompt = "\n".join(prompt_lines)

    try:
        response_text = call_gemini_text(prompt, system_instruction=system_instruction)
        return jsonify({"response": response_text}), 200
    except Exception as e:
        return jsonify({"error": f"Failed to fetch AI response: {str(e)}"}), 502


