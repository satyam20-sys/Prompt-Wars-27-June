# Constants
DATA_FILE = "journal_data.json"
MAX_JOURNAL_CHARS = 2000
MIN_BURNOUT_ENTRIES = 3
GEMINI_MODEL_NAME = "gemini-2.5-flash"

# Configure Gemini
api_key = os.environ.get("GEMINI_API_KEY")
if api_key:
    genai.configure(api_key=api_key)


@app.before_request
def check_api_key():
    """Enforces that the GEMINI_API_KEY environment variable is set for all API endpoints."""
    # Skip check for frontend static files and root route
    exempt_paths = ["/", "/index.html", "/static/style.css", "/static/script.js", "/favicon.ico"]
    if request.path in exempt_paths or request.path.startswith("/static/"):
        return None

    if not os.environ.get("GEMINI_API_KEY"):
        return jsonify({
            "error": "GEMINI_API_KEY is not configured on the server. Please check your .env file."
        }), 500


def sanitize_text(text):
    """Strips HTML tags from the given text and clips to maximum length."""
    if not text:
        return ""
    # Strip HTML tags
    clean = re.sub(r"<[^>]*>", "", text)
    # Return stripped and clipped text
    return clean.strip()[:MAX_JOURNAL_CHARS]


def read_journal_data():
    """Reads the journal JSON file. Initializes as empty array if missing.

    Raises ValueError if JSON is corrupt, which returns a 500 error.
    """
    if not os.path.exists(DATA_FILE):
        write_journal_data([])
        return []

    try:
        with open(DATA_FILE, "r", encoding="utf-8") as f:
            content = f.read().strip()
            if not content:
                return []
            return json.loads(content)
    except json.JSONDecodeError as e:
        raise ValueError(f"Database corruption: Failed to parse {DATA_FILE}. Detailed error: {str(e)}")
    except Exception as e:
        raise IOError(f"Database Read Error: Unable to access {DATA_FILE}. Error: {str(e)}")


def write_journal_data(data):
    """Writes the journal data array back to the JSON file."""
    try:
        with open(DATA_FILE, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2, ensure_ascii=False)
    except Exception as e:
        raise IOError(f"Database Write Error: Unable to save to {DATA_FILE}. Error: {str(e)}")


def call_gemini_json(prompt, system_instruction=None):
    """Calls Gemini 1.5 Flash expecting a structured JSON response."""
    try:
        generation_config = {"response_mime_type": "application/json"}
        model = genai.GenerativeModel(
            model_name=GEMINI_MODEL_NAME,
            system_instruction=system_instruction,
            generation_config=generation_config
        )
        response = model.generate_content(prompt)
        text = response.text.strip()

        # Clean markdown code fences if generated by accident
        if text.startswith("```json"):
            text = text[7:]
        if text.endswith("```"):
            text = text[:-3]
        text = text.strip()

        try:
            return json.loads(text)
        except json.JSONDecodeError as parse_err:
            raise ValueError(f"JSON Parse Failure on Gemini response. Error: {str(parse_err)}. Content: {text}")

    except Exception as e:
        raise RuntimeError(f"Gemini API failure: {str(e)}")


def call_gemini_text(prompt, system_instruction=None):
    """Calls Gemini 1.5 Flash expecting a plain text response."""
    try:
        model = genai.GenerativeModel(
            model_name=GEMINI_MODEL_NAME,
            system_instruction=system_instruction
        )
        response = model.generate_content(prompt)
        return response.text.strip()
    except Exception as e:
        raise RuntimeError(f"Gemini API failure: {str(e)}")


